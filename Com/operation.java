public class operation
{
	private Integer id;
	private String name;
	private String email;
	
	public void setId(Integer id)
	{
		this.id = id;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public void setEmail(String email)
	{
		this.email = email;
	}

	public Integer getId()
	{
		return this.id;
	}
	
	public String getName()
	{
		return this.name;
	}

	public String getEmail()
	{
		return this.email;
	}	
}